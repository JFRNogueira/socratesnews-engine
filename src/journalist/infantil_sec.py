    def get_news1_header(self):
        dia = self.date.strftime('%d')
        mes = ['janeiro', 'fevereiro', 'março', 'abril', 'maio', 'junho', 'julho', 'agosto', 'setembro', 'outubro', 'novembro', 'dezembro'][int(self.date.strftime('%m'))-1]
        if dia == '01':
            dia = '1º'
        news1_header = f'{dia} de {mes}: {self.theme}'
        return news1_header



    def get_news1_text(self):
        content = f'Escreva um texto voltado para crianças recém alfabetizadas com o tema {self.theme}. ' 
        content += f'Esse texto estará na seção infantil do dia {self.date.strftime('%d/%m')} ao lado de uma imagem em preto em branco sobre o tema (que será colorida).'
        content += f'O texto deve conter entre 800 e 900 caracteres. Escreva estritamente o corpo do texto.'
        
        client = openai.Client(api_key=os.getenv("OPENAI_API_KEY"))
        message = [{
            'role': 'user',
            'content': content
        }]
        response = client.chat.completions.create(
            messages=message, 
            model="gpt-4o-mini", 
            max_tokens=10000, 
            temperature=0, 
        )
        news_text = response.choices[0].message.content
        return news_text



    def get_news1_image_url(self):
        content = f'Crie uma imagem em preto e branco (no estilo line art) com o objetivo de ser colorida por crianças entre 2 e 6 anos. '
        content += f'A imagem deve estar relacionada ao tema {self.theme} e será divulgada na seção infantil de um jornal.\nO texto que estará ao lado da imagem é o seguinte:\n\n' 
        
        client = openai.Client(api_key=os.getenv("OPENAI_API_KEY"))

        response = client.images.generate(
            model="dall-e-3",
            prompt=content,
            size="1024x1024",
            quality="standard",
            n=1,
            )
        image_url = response.data[0].url
        return image_url



    def get_curiosity_text(self):
        theme = self.theme.lower().replace('dia', '').replace(' do ', '').replace(' da ', '').replace('internacional', '').replace('mundial', '').replace('nacional', '').replace('  ', ' ')
        content = f'Aponte-me duas curiosidades distintas para inserir na seção infantil de um jornal. '
        content += f'Forneça, como resposta, estritamente as curiosidades da maneira que cada uma delas deve conter 850 caracteres. '
        content += f'O tema é {theme}.\n\n'
        content += 'A resposta deve ser um JSON no seguinte formato: {"curiosity1": "Texto da curiosidade 1...", "curiosity2": "Texto da curiosidade 2..."}'
        client = openai.Client(api_key=os.getenv("OPENAI_API_KEY"))
        message = [{
            'role': 'user',
            'content': content
        }]
        response = client.chat.completions.create(
            messages=message, 
            model="gpt-4o-mini", 
            max_tokens=10000, 
            temperature=1, 
        )
        response_text = response.choices[0].message.content
        curiosities = json.loads(response_text)
        
        return curiosities['curiosity1'], curiosities['curiosity2']



    def get_curiosity_image(self, curiosity):
        prompt = f'Crie uma imagem infantil para ser colocara junto ao texto a seguir na seção infantil de um jornal de grande circulação.\n\n' 
        prompt += f'{curiosity}'
        
        response = client.images.generate(
            model="dall-e-3",
            prompt=prompt,
            size="1024x1024",
            quality="standard",
            n=1,
        )
        image_url = response.data[0].url
        return image_url



